package com.fss.ml.model;

public class CaisseForm {

	private double montantRetirer;
	private double montantAjouter;
	
	private Long idCaisse;
	
	

	
	public Long getIdCaisse() {
		return idCaisse;
	}
	public void setIdCaisse(Long idCaisse) {
		this.idCaisse = idCaisse;
	}
	public double getMontantRetirer() {
		return montantRetirer;
	}
	public void setMontantRetirer(double montantRetirer) {
		this.montantRetirer = montantRetirer;
	}
	public double getMontantAjouter() {
		return montantAjouter;
	}
	public void setMontantAjouter(double montantAjouter) {
		this.montantAjouter = montantAjouter;
	}
	
	
}
